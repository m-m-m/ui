/**
 * Available sizing for a {@link io.github.mmm.ui.widget.window.UiAbstractWindow window}.
 */
public enum UiWindowSizing {

  /** {@link io.github.mmm.ui.attribute.AttributeWriteMinimized#isMinimized() Minimized} state. */
  MINIMIZED,

  /** The normal state (default). */
  NORMAL,

  /** {@link io.github.mmm.ui.attribute.AttributeWriteMaximized#isMaximized() Maximized} state. */
  MAXIMIZED

}
